import React from "react";
import { useStaticQuery, Link, graphql } from "gatsby";

export default function Home() {
  const data = useStaticQuery(
    graphql`
      query {
        site {
          siteMetadata {
            title
          }
        }
      }
    `
  );
  return (
    <div>
      <h1>{data.site.siteMetadata.title}</h1>
    </div>
  );
}




